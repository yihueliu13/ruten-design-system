(async () => {
  // 1. Find ruten collection
  const collections = figma.variables.getLocalVariableCollections();
  const collection = collections.find(c => c.name.toLowerCase() === "ruten");
  if (!collection) {
    console.log("❌ Collection 'ruten' not found. Available:", collections.map(c => c.name));
    return;
  }
  const modeId = collection.modes[0].id;
  console.log(`✅ Found collection "${collection.name}", mode: ${modeId}`);

  // 2. Build existing variable name → variable map
  const allVars = figma.variables.getLocalVariables();
  const varMap = new Map();
  for (const v of allVars) {
    if (v.variableCollectionId === collection.id) {
      varMap.set(v.name, v);
    }
  }
  console.log(`📦 Existing variables in collection: ${varMap.size}`);

  // 3. Process tokens
  let created = 0;
  let skipped = 0;
  let failed = 0;
  const newVarMap = new Map(); // track newly created vars for alias resolution

  for (const token of TOKENS) {
    try {
      // Existence check
      if (varMap.has(token.p)) {
        skipped++;
        continue;
      }

      // Create variable
      const variable = figma.variables.createVariable(token.p, collection, token.t);
      varMap.set(token.p, variable);
      newVarMap.set(token.p, variable);

      // Set value
      const val = token.v;
      if (typeof val === "string" && val.startsWith("{") && val.endsWith("}")) {
        // Alias reference
        const targetName = aliasPathToName(val);
        const targetVar = varMap.get(targetName);
        if (targetVar) {
          variable.setValueForMode(modeId, figma.variables.createVariableAlias(targetVar));
        } else {
          console.log(`⚠️ Alias target not found: ${targetName} (for ${token.p}), setting fallback`);
          if (token.t === "COLOR") {
            variable.setValueForMode(modeId, { r: 0, g: 0, b: 0, a: 0 });
          } else {
            variable.setValueForMode(modeId, 0);
          }
        }
      } else if (typeof val === "string" && val.includes("#")) {
        // Raw color hex string
        variable.setValueForMode(modeId, hexToFigmaColor(val));
      } else if (typeof val === "number") {
        // Raw number
        variable.setValueForMode(modeId, val);
      } else {
        console.log(`⚠️ Unknown value format for ${token.p}: ${val}`);
        variable.setValueForMode(modeId, token.t === "COLOR" ? { r: 0, g: 0, b: 0, a: 0 } : 0);
      }

      created++;
    } catch (err) {
      console.log(`❌ Failed: ${token.p} — ${err.message || err}`);
      failed++;
    }
  }

  console.log("");
  console.log(`✅ Done: ${created} created, ${skipped} skipped, ${failed} failed`);
  console.log(`📊 Total variables in collection: ${varMap.size}`);
})();
