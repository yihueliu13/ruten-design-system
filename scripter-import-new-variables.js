// ─── Helpers ───────────────────────────────────────────────

function hexToFigmaColor(hexAlpha) {
  // Format: "#RRGGBB|alpha" or just "#RRGGBB"
  const parts = hexAlpha.split("|");
  const hex = parts[0].replace("#", "");
  const alpha = parts.length > 1 ? parseFloat(parts[1]) : 1;
  const r = parseInt(hex.substring(0, 2), 16) / 255;
  const g = parseInt(hex.substring(2, 4), 16) / 255;
  const b = parseInt(hex.substring(4, 6), 16) / 255;
  return { r, g, b, a: alpha };
}

function aliasPathToName(aliasStr) {
  // "{sys.sizing.control-height.default}" → "sys/sizing/control-height/default"
  return aliasStr.replace(/[{}]/g, "").replace(/\./g, "/");
}

